package com.tutorials.tree;

public class DiaHeight {

	private int height;
	private int maxDia;

	public DiaHeight(int height, int maxDia) {
		super();
		this.height = height;
		this.maxDia = maxDia;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

	public int getMaxDia() {
		return maxDia;
	}

	public void setMaxDia(int maxDia) {
		this.maxDia = maxDia;
	}

}
